public class CalcOperations {
    double add(double a, double b) {
        double answer = a+b;
        return answer;
    }

    double subtract(double a, double b) {
        double answer = a-b;
        return answer;
    }

    double multiply(double a, double b) {
        double answer = a*b;
        return answer;
    }

    double divide(double a, double b) {
        double answer = a/b;
        return answer;
    }
}
